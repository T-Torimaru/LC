#include <DaqRunSummaryBlock.hh>

namespace CALICE {

  void DaqRunSummaryBlock::print(std::ostream& os) {
    std::cout << "Sun Summary for Run: " << getIntVal(kDaqRunNum) << std::endl;
    std::cout << "Number of Configurations: " << getIntVal(kDaqNumConf) << std::endl;
    std::cout << "Number of Slowreadouts: " << getIntVal(kDaqNumSro) << std::endl;
    std::cout << "Number of Acquisitions: " << getIntVal(kDaqNumAcq) << std::endl;
    std::cout << "Number of Daq Events: " << getIntVal(kDaqNumEvt) << std::endl;
    std::cout << "Duration of Run: " << getIntVal(kDaqRunTime) << " s" << std::endl;
   }


}
