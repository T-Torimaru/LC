#include <AhcSlowReadoutBlock.hh>

namespace CALICE {
  
  void AhcSlowReadoutBlock::print(std::ostream& os) {
    os << "Ahc SlowReadout Data" << std::endl;
    os << "Moving Stage Position" << std::endl;
    os << "x Position[mm]: " << get_xPosition_mm() << std::endl;
    os << "y Position[mm]: " << get_yPosition_mm() << std::endl;
    os << "z Position[mm]: " << get_zPosition_mm() << std::endl;
    os << "rotated Position[deg]: " << get_cPosition_deg() << std::endl;
  }
  
}
